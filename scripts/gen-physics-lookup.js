import { writeFileSync } from 'node:fs';
import { PHYSICS_PRESETS } from '../src/engine/physicsConstants.js';

const EV_MIN = 60;
const EV_MAX = 115;
const LA_MIN = -10;
const LA_MAX = 50;
const cfg = PHYSICS_PRESETS.sim;

function calcBallDist(ev, la) {
  const evMs = ev * 0.44704;
  const laRad = la * (Math.PI / 180);
  let vx = evMs * Math.cos(laRad);
  let vy = evMs * Math.sin(laRad);
  let x = 0;
  let y = cfg.releaseHeight;
  for (let i = 1; i <= cfg.maxSteps; i += 1) {
    const speed = Math.max(0.0001, Math.hypot(vx, vy));
    const dragAccel = cfg.dragCoeff * speed * speed;
    vx -= dragAccel * (vx / speed) * cfg.dt;
    vy -= (cfg.gravity + dragAccel * (vy / speed)) * cfg.dt;
    x += vx * cfg.dt;
    y += vy * cfg.dt;
    if (y <= 0) return Math.max(0, Math.round(x));
  }
  return Math.max(0, Math.round(x));
}

const rows = [];
for (let ev = EV_MIN; ev <= EV_MAX; ev += 1) {
  const row = [];
  for (let la = LA_MIN; la <= LA_MAX; la += 1) row.push(calcBallDist(ev, la));
  rows.push(row);
}

const output = `// Auto-generated by scripts/gen-physics-lookup.js — do not edit manually.\n// physicsConstants.js を更新したら再生成: node scripts/gen-physics-lookup.js\n\nconst EV_MIN = ${EV_MIN};\nconst EV_MAX = ${EV_MAX};\nconst LA_MIN = ${LA_MIN};\nconst LA_MAX = ${LA_MAX};\n\nexport const DIST_TABLE = ${JSON.stringify(rows)};\n\nexport function lookupBallDist(ev, la) {\n  const safeEv = Number.isFinite(ev) ? ev : EV_MIN;\n  const safeLa = Number.isFinite(la) ? la : LA_MIN;\n  const evIdx = Math.round(Math.min(Math.max(safeEv, EV_MIN), EV_MAX)) - EV_MIN;\n  const laIdx = Math.round(Math.min(Math.max(safeLa, LA_MIN), LA_MAX)) - LA_MIN;\n  return DIST_TABLE[evIdx][laIdx];\n}\n`;
writeFileSync(new URL('../src/engine/physicsLookup.js', import.meta.url), output, 'utf8');
console.log('generated physics lookup');
